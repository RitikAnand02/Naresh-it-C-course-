#include <stdio.h>
int main()
{
    int n, r, c;

    printf("Enter no row ");
    scanf("%d", &n);
    for (r = 1; r <= n; r++)
    {

        for (c = 1; c <= n; c++)
        {
            if (c <= n - r)
            {
                printf("%3d", 1);
            }

            else
            {
                printf("%3d", r);
            }
        }
        printf("\n");
    }
}