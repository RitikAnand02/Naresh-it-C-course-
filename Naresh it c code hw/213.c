    for (r = 1; r <= nr; r++)
    {
        char l = 'a', u = 'A';
        for (c = 1; c <= nc; c++)
        {
            if (c % 2 == 0)
            {
                printf("%2c", l++);
            }
            else
            {
                printf("%2c", u++);
            }
        }
        printf("\n");
    }
