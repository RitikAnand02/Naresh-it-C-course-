#include <stdio.h>
int main()
{
    int n, r, c, a = 1;

    printf("Enter no row ");
    scanf("%d", &n);
    for (r = 1; r <= n; r++)
    {

        for (c = 1; c <= n; c++)
        {
            if (c == 1 || r == 1 || c == n || r == n)
            {
                printf("%3d", a++);
            }

            else
            {
                printf("%3c", '*');
            }
        }
        printf("\n");
    }
}